           java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
        String UserName = jTextField1.getText();
        String UserLastName = jTextField2.getText();
        String UserMail = jTextField3.getText()+ jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
        String UserPassword = jTextField4.getText();
        
        if(!UserName.equals("") || !UserMail.equals("") || !UserPassword.equals("")) {
            String insert = "INSERT INTO charchatdb.users(users.UserName, users.UserLastName, users.UserMail, users.UserPassword, users.UserIPAddress)"
                + "values ('"+UserName+"','"+UserLastName+"','"+UserMail+"','"+UserPassword+"','"+UserPassword+"')";
            netcon.sendSQL(insert);
            new CCGUI().setVisible(true);
            LoginFrame.this.dispose();
        } else {
            jLabel5.setForeground(Color.RED);
            jLabel5.setText("Поля Ім'я, Почта та Пароль обов'язкові для заповнення!");
        }    
            }
        });
        
