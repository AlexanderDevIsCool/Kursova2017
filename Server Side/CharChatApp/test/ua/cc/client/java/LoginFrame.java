    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
        
        if(jTextField5.getText().equals("") || jPasswordField1.getPassword().equals("")) {
            jLabel9.setForeground(Color.RED);
            jLabel9.setText("Поле логіна або пароля не може бути пустим");
        } else {
           char[] pass = jPasswordField1.getPassword();
           String aa = "";
           for(int i = 0; i < pass.length; i++) {
               aa += pass[i];
           }
           netcon.sendSQL("UPDATE users SET UserIPAddress = '"+netcon.socket.getInetAddress().toString()+"' WHERE UserMail = '"+jTextField5.getText()+
                   "' and UserPassword = '"+aa+"';");
           netcon.sendSQL("SELECT UserID, UserIPAddress FROM users WHERE UserMail = '"+jTextField5.getText()+"' and UserPassword = '"+aa+"'");
           jLabel9.setText("ServerSay:"+serverSay);
        }
             if(serverSay.equalsIgnoreCase("true")) {
                LoginFrame.this.dispose();
               new CCGUI().setVisible(true);
           } else {
               if(serverSay == null) {
                   jLabel9.setForeground(Color.RED);
                   jLabel9.setText("Неправильний логін або пароль");
               }
           }
            }
        });
    }//GEN-LAST:event_jButton4ActionPerformed

